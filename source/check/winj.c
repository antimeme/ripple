static int
check_except(JNIEnv *env)
{
  int result = EXIT_SUCCESS;
  if (env && *env) {
    if ((*env)->ExceptionCheck(env)) {
      (*env)->ExceptionDescribe(env);
      (*env)->ExceptionClear(env);
      result = EXIT_FAILURE;
    }
  }
  return result;
}

static int
fail(JNIEnv *env, const char *format, ...)
{
  va_list args;

  check_except(env);

  va_start(args, format);
  fprintf(stderr, "ERROR: ");
  vfprintf(stderr, format, args);
  fprintf(stderr, "\n");
  va_end(args);
  return EXIT_FAILURE;
}

int
invoke(int argc, char **argv)
{
  int result = EXIT_SUCCESS;
  jint rc;
  JavaVM *jvm = NULL;
  JNIEnv *env = NULL;
  JavaVMInitArgs vm_args;
  JavaVMOption options[1];
  jclass main_class;
  jmethodID main_method;
  jobjectArray args_array;
  jclass string_class;

  memset(&vm_args, 0, sizeof(vm_args));
  vm_args.version = JNI_VERSION_1_8;

  if ((rc = JNI_GetDefaultJavaVMInitArgs(&vm_args)) < 0) {
    result = fail(env, "JVM does not support Java 1.8: %d", rc);
  } else {
    vm_args.nOptions = sizeof(options)/sizeof(*options);
    vm_args.options  = options;
    vm_args.ignoreUnrecognized = JNI_FALSE;
  }

  if (rc < 0) {
  } else if ((rc = JNI_CreateJavaVM
              (&jvm, (void**)&env, &vm_args)) < 0) {
    result = fail(env, "failed to create JVM: %d", rc);
  } else if (!env || !*env) {
    result = fail(env, "got NULL for JNI environment");
  } else if ((rc = (*env)->GetVersion(env)) < 0) {
    result = fail(env, "failure from GetVersion (%d)", rc);
  } else if (argc && !(main_class = (*env)->FindClass(env, argv[0]))) {
    result = fail(env, "failed to find class %s", argv[0]);
  } else if (!argc && !(main_class = (*env)->DefineClass
                        (env, "Check", NULL, check_class,
                         sizeof(check_class)))) {
    result = fail(env, "failed to find Check class");
  } else if (!(main_method = (*env)->GetStaticMethodID
               (env, main_class, "main", "([Ljava/lang/String;)V"))) {
    result = fail(env, "failed to find main method");
  } else if (!(string_class = (*env)->FindClass
               (env, "java/lang/String"))) {
    result = fail(env, "failed to find string class");
  } else if (!(args_array = (*env)->NewObjectArray
               (env, !argc ? argc : (argc - 1), string_class, NULL))) {
    result = fail(env, "failed to create argument array");
  } else {
    size_t ii;

    for (ii = 1; (result == EXIT_SUCCESS) && (ii < argc); ++ii) {
      jstring jstr = (*env)->NewStringUTF(env, argv[ii]);

      if (jstr) {
        (*env)->SetObjectArrayElement(env, args_array, ii - 1, jstr);
        (*env)->DeleteLocalRef(env, jstr);
      } else result = fail
               (env, "failed to create Java string from \"%u\"", ii);
    }
  }

  if (EXIT_SUCCESS != result) {
  } else {
    (*env)->CallStaticVoidMethod
      (env, main_class, main_method, args_array);
    if ((*env)->ExceptionCheck(env))
      result = fail(env, "exception occurred in main method");
  }

  if (env && *env) {
    (*env)->DeleteLocalRef(env, args_array);
    (*env)->DeleteLocalRef(env, string_class);
    (*env)->DeleteLocalRef(env, main_class);
  }
  if (jvm)
    (*jvm)->DestroyJavaVM(jvm);
  return result;
}

int
main(int argc, char **argv)
{
  int result = EXIT_SUCCESS;
  if (argc < 2)
    result = invoke(0, NULL);
  else if (!strcmp("main", argv[1]))
    result = invoke(argc - 2, argv + 2);
  else result = fail(NULL, "unrecognized task \"%s\"", argv[1]);
  return result;
}
